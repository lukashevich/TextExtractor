import UIKit

extension UIImage {
  static var waveformCircle: UIImage {
    UIImage(systemName: "waveform.circle") ?? UIImage()
  }
  
  static var doc: UIImage {
    UIImage(systemName: "doc") ?? UIImage()
  }
  
  static var trash: UIImage {
    UIImage(systemName: "trash") ?? UIImage()
  }
}
